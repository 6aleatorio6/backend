function cadastrarImagensGaleria(datCat) {
    const catalogoGaleria = [];

    datCat.forEach(item => {
        item.fotosGaleria.forEach(foto => {
            catalogoGaleria.push({
                uuid: item.uuid,
                imagem: foto
            });
        });
    });

    return catalogoGaleria;
}

